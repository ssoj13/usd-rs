//! usdstitchclips - Stitch USD files using value clips
//!
//! Port of pxr/usd/bin/usdstitchclips/usdstitchclips.py
//! and pxr/usd/usdUtils/stitchClips.cpp
//!
//! Combines multiple USD files using the value clips composition mechanism.
//! This is different from regular stitching - clips allow time-varying data
//! to be split across multiple files for efficient storage and loading.

use std::path::Path;

use usd::sdf::path::Path as SdfPath;
use usd::sdf::Layer;
use usd::tf::Token;
use usd::usd_utils::stitch_clips::{
    generate_clip_manifest_name, generate_clip_topology_name, stitch_clips, stitch_clips_template,
};

/// Run the stitchclips command
pub fn run(args: &[String]) -> i32 {
    match run_impl(args) {
        Ok(()) => 0,
        Err(e) => {
            eprintln!("Error: {}", e);
            1
        }
    }
}

fn run_impl(args: &[String]) -> Result<(), String> {
    let mut output_file: Option<String> = None;
    let mut clip_path: Option<String> = None;
    let mut start_time: Option<f64> = None;
    let mut end_time: Option<f64> = None;
    let mut stride: Option<f64> = None;
    let mut template_path: Option<String> = None;
    let mut clip_set_name: Option<String> = None;
    let mut no_comment = false;
    let mut interpolate_missing = false;
    let mut input_files: Vec<String> = Vec::new();

    let mut i = 1;
    while i < args.len() {
        match args[i].as_str() {
            "-h" | "--help" => {
                print_help();
                return Ok(());
            }
            "-o" | "--out" => {
                i += 1;
                if i >= args.len() {
                    return Err("--out requires a file path".into());
                }
                output_file = Some(args[i].clone());
            }
            "-c" | "--clipPath" => {
                i += 1;
                if i >= args.len() {
                    return Err("--clipPath requires a prim path".into());
                }
                clip_path = Some(args[i].clone());
            }
            "-s" | "--startTimeCode" => {
                i += 1;
                if i >= args.len() {
                    return Err("--startTimeCode requires a number".into());
                }
                start_time = Some(args[i].parse().map_err(|_| "Invalid start time")?);
            }
            "-e" | "--endTimeCode" => {
                i += 1;
                if i >= args.len() {
                    return Err("--endTimeCode requires a number".into());
                }
                end_time = Some(args[i].parse().map_err(|_| "Invalid end time")?);
            }
            "--stride" => {
                i += 1;
                if i >= args.len() {
                    return Err("--stride requires a number".into());
                }
                stride = Some(args[i].parse().map_err(|_| "Invalid stride")?);
            }
            "--templatePath" => {
                i += 1;
                if i >= args.len() {
                    return Err("--templatePath requires a path template".into());
                }
                template_path = Some(args[i].clone());
            }
            "--clipSet" => {
                i += 1;
                if i >= args.len() {
                    return Err("--clipSet requires a name".into());
                }
                clip_set_name = Some(args[i].clone());
            }
            "-n" | "--noComment" => no_comment = true,
            "--interpolateMissingClipValues" => interpolate_missing = true,
            arg if arg.starts_with('-') => {
                return Err(format!("Unknown option: {}", arg));
            }
            _ => input_files.push(args[i].clone()),
        }
        i += 1;
    }

    // Validate required args
    let output = output_file.ok_or("Output file (-o) is required")?;
    let clip_prim_path_str = clip_path.ok_or("Clip path (-c) is required")?;

    if input_files.is_empty() {
        return Err("At least one input file is required".into());
    }

    // Parse clip path
    let clip_prim_path = SdfPath::from_string(&clip_prim_path_str)
        .ok_or_else(|| format!("Invalid clip path: {}", clip_prim_path_str))?;

    // Verify input files exist
    for file in &input_files {
        if !Path::new(file).exists() {
            return Err(format!("Input file not found: {}", file));
        }
    }

    // Initialize SDF
    usd::sdf::init();

    // Create result layer
    let result_layer = Layer::create_new(&output)
        .map_err(|e| format!("Failed to create output layer: {:?}", e))?;

    // Add comment unless disabled
    if !no_comment {
        result_layer.set_comment(format!(
            "Generated by usdstitchclips from {} clip files",
            input_files.len()
        ));
    }

    let clip_set_token = clip_set_name.as_ref().map(|s| Token::new(s));

    // Use template-based or direct stitching
    let success = if let Some(ref tmpl) = template_path {
        // Template-based clip stitching
        let topology_name = generate_clip_topology_name(&output);
        let manifest_name = generate_clip_manifest_name(&output);

        let topology_layer = Layer::find_or_open(&topology_name)
            .unwrap_or_else(|_| Layer::create_anonymous(Some("topology")));
        let manifest_layer = Layer::find_or_open(&manifest_name)
            .unwrap_or_else(|_| Layer::create_anonymous(Some("manifest")));

        let s = start_time.unwrap_or(0.0);
        let e = end_time.unwrap_or(100.0);
        let st = stride.unwrap_or(1.0);

        stitch_clips_template(
            &result_layer,
            &topology_layer,
            &manifest_layer,
            &clip_prim_path,
            tmpl,
            s,
            e,
            st,
            None, // active_offset
            interpolate_missing,
            clip_set_token.as_ref(),
        )
    } else {
        // Direct clip stitching
        stitch_clips(
            &result_layer,
            &input_files,
            &clip_prim_path,
            start_time,
            end_time,
            interpolate_missing,
            clip_set_token.as_ref(),
        )
    };

    if !success {
        return Err("Clip stitching failed".into());
    }

    // Save result
    result_layer
        .save()
        .map_err(|e| format!("Failed to save: {:?}", e))?;

    println!("Created: {}", output);

    // Report generated files
    let topology_name = generate_clip_topology_name(&output);
    let manifest_name = generate_clip_manifest_name(&output);
    if Path::new(&topology_name).exists() {
        println!("Created: {}", topology_name);
    }
    if Path::new(&manifest_name).exists() {
        println!("Created: {}", manifest_name);
    }

    Ok(())
}

fn print_help() {
    println!(
        r#"usdstitchclips - Stitch USD files using value clips

USAGE:
    usd stitchclips [OPTIONS] -o <output> -c <clipPath> <input_files...>

Combines multiple USD files using the value clips composition mechanism.
This allows time-varying data to be split across multiple files for
efficient storage and streaming.

REQUIRED:
    -o, --out <file>           Output file path
    -c, --clipPath <path>      Prim path for clips (e.g., /Model)
    <input_files...>           Input clip files

OPTIONS:
    -s, --startTimeCode <t>    Start time code (default: from clips)
    -e, --endTimeCode <t>      End time code (default: from clips)
    --stride <n>               Time stride between clips (for templates)
    --templatePath <path>      Template string for clip asset paths
                               e.g., "./clips/clip.###.usd"
    --clipSet <name>           Clip set name (default: "default")
    --interpolateMissingClipValues
                               Interpolate values for clips without samples
    -n, --noComment            Don't add comment to output file
    -h, --help                 Show this help

OUTPUT FILES:
    <output>                   Main result layer with clip metadata
    <output>.topology.usd      Aggregated scene topology
    <output>.manifest.usd      Attribute manifest for clips

EXAMPLES:
    # Basic clip stitching
    usd stitchclips -o result.usd -c /Model clip1.usd clip2.usd clip3.usd

    # With explicit time range
    usd stitchclips -o result.usd -c /Model -s 0 -e 100 clips/*.usd

    # Template-based clips (for large sequences)
    usd stitchclips -o result.usd -c /Model --templatePath "./clip.###.usd" \
        --stride 1 -s 1 -e 100 clip.001.usd

    # Named clip set
    usd stitchclips -o result.usd -c /Char --clipSet animation clips/*.usd

VALUE CLIPS:
    Value clips allow time-varying attribute data to be stored across
    multiple files. When a stage is opened, USD will dynamically load
    the appropriate clip based on the current time code.

    This is useful for:
    - Large animation sequences (one file per frame)
    - Simulation cache playback
    - Efficient streaming of time-varying data
"#
    );
}
