#[test]
fn test_detached_asset() {
    let mut tmp = tempfile::NamedTempFile::new().expect("failed to create temp file");
    let contents = b"detach test data";
    tmp.write_all(contents).expect("write failed");
    tmp.flush().expect("flush failed");

    let asset = FilesystemAsset::open(tmp.path()).expect("open failed");
    let detached = asset.get_detached().expect("get_detached failed");

    // Detached asset should have same size and contents
    assert_eq!(detached.size(), contents.len());
    let buf = detached.get_buffer().expect("buffer failed");
    assert_eq!(&*buf, contents);

    // Detached should be independent — drop original file
    drop(tmp);
    // Detached should still work
    assert_eq!(detached.size(), contents.len());
}
