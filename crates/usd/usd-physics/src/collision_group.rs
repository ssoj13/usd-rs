/// Lightweight wrapper representing the CollectionAPI:colliders interface
/// on a CollisionGroup prim.
///
/// Matches C++ `UsdCollectionAPI` returned by
/// `UsdPhysicsCollisionGroup::GetCollidersCollectionAPI()`.
#[derive(Debug, Clone)]
pub struct CollisionGroupCollectionAPI {
    prim: Prim,
    collection_name: Token,
}

impl CollisionGroupCollectionAPI {
    /// Returns the collection name ("colliders").
    pub fn get_name(&self) -> &Token {
        &self.collection_name
    }

    /// Returns the wrapped prim.
    pub fn get_prim(&self) -> &Prim {
        &self.prim
    }

    /// Returns the includes relationship for this collection.
    pub fn get_includes_rel(&self) -> Option<Relationship> {
        let rel_name = format!("collection:{}:includes", self.collection_name.get_text());
        self.prim.get_relationship(&rel_name)
    }

    /// Returns the excludes relationship for this collection.
    pub fn get_excludes_rel(&self) -> Option<Relationship> {
        let rel_name = format!("collection:{}:excludes", self.collection_name.get_text());
        self.prim.get_relationship(&rel_name)
    }

    /// Returns the expansion rule attribute for this collection.
    pub fn get_expansion_rule_attr(&self) -> Option<Attribute> {
        let attr_name = format!(
            "collection:{}:expansionRule",
            self.collection_name.get_text()
        );
        self.prim.get_attribute(&attr_name)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_schema_kind() {
        assert_eq!(CollisionGroup::SCHEMA_KIND, SchemaKind::ConcreteTyped);
    }

    #[test]
    fn test_schema_type_name() {
        assert_eq!(CollisionGroup::SCHEMA_TYPE_NAME, "PhysicsCollisionGroup");
    }

    #[test]
    fn test_collision_group_table_default() {
        let table = CollisionGroupTable::default();
        assert!(table.get_collision_groups().is_empty());
    }

    #[test]
    fn test_schema_attribute_names() {
        let names = CollisionGroup::get_schema_attribute_names(false);
        assert!(names.iter().any(|n| n.get_text() == "physics:mergeGroup"));
        assert!(
            names
                .iter()
                .any(|n| n.get_text() == "physics:invertFilteredGroups")
        );
    }

    // -------------------------------------------------------------------------
    // CollisionGroupTable unit tests — test the table logic directly by
    // constructing it manually (bypassing stage traversal).
    // -------------------------------------------------------------------------

    /// Build a CollisionGroupTable manually for N groups with given filter rules.
    ///
    /// `filter_pairs` is a list of (a, b) index pairs where collision is disabled.
    /// Uses the upper-triangular layout matching C++: size = (n+1)*n/2.
    fn make_table(n: usize, filter_pairs: &[(usize, usize)]) -> CollisionGroupTable {
        let groups: Vec<Path> = (0..n)
            .map(|i| Path::from_string(&format!("/Group{}", i)).unwrap())
            .collect();
        let tri_size = (n + 1) * n / 2;
        let mut enabled = vec![true; tri_size];
        for &(a, b) in filter_pairs {
            // Store only upper-triangle (min, max)
            let ti = CollisionGroupTable::table_index(a, b, n);
            enabled[ti] = false;
        }
        CollisionGroupTable { groups, enabled }
    }

    #[test]
    fn test_table_two_groups_no_filter() {
        // 2 groups, no filter: all pairs enabled
        let table = make_table(2, &[]);
        assert!(table.is_collision_enabled_by_index(0, 0));
        assert!(table.is_collision_enabled_by_index(0, 1));
        assert!(table.is_collision_enabled_by_index(1, 0));
        assert!(table.is_collision_enabled_by_index(1, 1));
    }

    #[test]
    fn test_table_two_groups_filtered() {
        // Groups 0 and 1 are filtered: collision disabled
        let table = make_table(2, &[(0, 1)]);
        assert!(!table.is_collision_enabled_by_index(0, 1));
        assert!(!table.is_collision_enabled_by_index(1, 0)); // symmetric
        assert!(table.is_collision_enabled_by_index(0, 0)); // self still enabled
        assert!(table.is_collision_enabled_by_index(1, 1));
    }

    #[test]
    fn test_table_three_groups_merge_scenario() {
        // 3 groups; groups 0 and 2 are filtered against group 1.
        // This simulates the "merge groups" logical index scenario:
        // mergeGroup="A" assigned to group 0 and group 2 -> they act as one.
        // Group 1 filters the merged group -> both (0,1) and (2,1) disabled.
        let table = make_table(3, &[(0, 1), (2, 1)]);

        assert!(!table.is_collision_enabled_by_index(0, 1));
        assert!(!table.is_collision_enabled_by_index(1, 0));
        assert!(!table.is_collision_enabled_by_index(2, 1));
        assert!(!table.is_collision_enabled_by_index(1, 2));

        // (0, 2) is not filtered
        assert!(table.is_collision_enabled_by_index(0, 2));
        assert!(table.is_collision_enabled_by_index(2, 0));
    }

    #[test]
    fn test_table_invert_filtered_groups_semantic() {
        // invertFilteredGroups: group 0 disables all EXCEPT group 1.
        // Equivalent to filtering (0,2), (0,3), ... for n=4
        let table = make_table(4, &[(0, 2), (0, 3)]);

        // (0, 1) remains enabled (the "allowed" group)
        assert!(table.is_collision_enabled_by_index(0, 1));
        assert!(table.is_collision_enabled_by_index(1, 0));

        // (0, 2) and (0, 3) are disabled
        assert!(!table.is_collision_enabled_by_index(0, 2));
        assert!(!table.is_collision_enabled_by_index(0, 3));

        // (1, 2) is unaffected
        assert!(table.is_collision_enabled_by_index(1, 2));
    }

    #[test]
    fn test_table_is_collision_enabled_by_path() {
        let table = make_table(3, &[(0, 2)]);
        let group0 = Path::from_string("/Group0").unwrap();
        let group1 = Path::from_string("/Group1").unwrap();
        let group2 = Path::from_string("/Group2").unwrap();

        assert!(table.is_collision_enabled(&group0, &group1));
        assert!(!table.is_collision_enabled(&group0, &group2));
        assert!(!table.is_collision_enabled(&group2, &group0)); // symmetric

        // Unknown group -> defaults to enabled
        let unknown = Path::from_string("/Unknown").unwrap();
        assert!(table.is_collision_enabled(&group0, &unknown));
    }

    #[test]
    fn test_table_out_of_bounds_index() {
        let table = make_table(2, &[]);
        // Out-of-bounds indices must return true (default enabled)
        assert!(table.is_collision_enabled_by_index(0, 99));
        assert!(table.is_collision_enabled_by_index(99, 0));
    }

    #[test]
    fn test_table_n_groups_all_filtered() {
        // n groups, every pair filtered
        let n = 4;
        let mut pairs = Vec::new();
        for i in 0..n {
            for j in (i + 1)..n {
                pairs.push((i, j));
            }
        }
        let table = make_table(n, &pairs);
        for i in 0..n {
            for j in 0..n {
                if i != j {
                    assert!(
                        !table.is_collision_enabled_by_index(i, j),
                        "({}, {}) should be disabled",
                        i,
                        j
                    );
                }
            }
        }
    }
}
