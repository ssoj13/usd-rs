//! Physics Collision Group schema.
//!
//! Defines a collision group for coarse filtering. When a collision occurs
//! between two objects that have a PhysicsCollisionGroup assigned, they will
//! collide with each other unless this PhysicsCollisionGroup pair is filtered.
//!
//! # C++ Reference
//!
//! Port of `pxr/usd/usdPhysics/collisionGroup.h` and `collisionGroup.cpp`
//!
//! # Usage
//!
//! ```ignore
//! use usd::usd_physics::CollisionGroup;
//!
//! // Create two collision groups
//! let group_a = CollisionGroup::define(&stage, &path_a)?;
//! let group_b = CollisionGroup::define(&stage, &path_b)?;
//!
//! // Filter collisions between groups
//! group_a.create_filtered_groups_rel()?.add_target(&path_b)?;
//! ```

use std::collections::{HashMap, HashSet};
use std::sync::Arc;

use usd_core::attribute::Variability;
use usd_core::{Attribute, Prim, Relationship, SchemaKind, Stage, Typed};
use usd_sdf::Path;
use usd_tf::Token;
use usd_vt::Value;

use super::tokens::USD_PHYSICS_TOKENS;

/// Collision group table for querying filter relationships.
///
/// Utility structure generated by `compute_collision_group_table()`.
/// Contains a table describing which pairs of collision groups have
/// collisions enabled/disabled by the filtering rules.
///
/// Uses an upper-triangular (diagonal) layout identical to C++:
/// table size = (n+1)*n/2, index = _get_table_index(min, max, n).
#[derive(Debug, Clone, Default)]
pub struct CollisionGroupTable {
    /// All collision groups in the table (in stage traversal order).
    groups: Vec<Path>,
    /// Upper-triangular collision enabled table.
    /// Size = (n+1)*n/2 where n = groups.len().
    /// Entry is false if collision is disabled.
    enabled: Vec<bool>,
}

impl CollisionGroupTable {
    /// Return the set of all CollisionGroup paths this table contains.
    pub fn get_collision_groups(&self) -> &[Path] {
        &self.groups
    }

    /// Compute the upper-triangular table index for (idx_a, idx_b).
    /// Identical to C++ `_GetCollisionTableIndex(minGroup, maxGroup, groupSize)`.
    fn table_index(idx_a: usize, idx_b: usize, n: usize) -> usize {
        let min = idx_a.min(idx_b);
        let max = idx_a.max(idx_b);
        // skip = min*(min+1)/2 entries in the rows above
        let num_skipped = (min * min + min) / 2;
        min * n - num_skipped + max
    }

    /// Return true if the groups at indices `idx_a` and `idx_b` collide.
    ///
    /// Out-of-bounds indices return true (default enabled), matching C++.
    pub fn is_collision_enabled_by_index(&self, idx_a: usize, idx_b: usize) -> bool {
        let n = self.groups.len();
        if idx_a >= n || idx_b >= n {
            // Groups not in table -> default enabled
            return true;
        }
        let index = Self::table_index(idx_a, idx_b, n);
        self.enabled.get(index).copied().unwrap_or(true)
    }

    /// Return true if the groups at paths `prim_a` and `prim_b` collide.
    pub fn is_collision_enabled(&self, prim_a: &Path, prim_b: &Path) -> bool {
        let idx_a = self.groups.iter().position(|p| p == prim_a);
        let idx_b = self.groups.iter().position(|p| p == prim_b);

        match (idx_a, idx_b) {
            (Some(a), Some(b)) => self.is_collision_enabled_by_index(a, b),
            _ => true, // Default to enabled for unknown groups
        }
    }
}

/// Physics collision group schema.
///
/// Defines a collision group for coarse filtering. When a collision
/// occurs between two objects that have a PhysicsCollisionGroup assigned,
/// they will collide with each other unless this PhysicsCollisionGroup pair
/// is filtered. See filteredGroups attribute.
///
/// A CollectionAPI:colliders maintains a list of PhysicsCollisionAPI rel-s
/// that defines the members of this CollisionGroup.
///
/// # Schema Kind
///
/// This is a concrete typed schema (ConcreteTyped).
///
/// # C++ Reference
///
/// Port of `UsdPhysicsCollisionGroup` class.
#[derive(Debug, Clone)]
pub struct CollisionGroup {
    prim: Prim,
}

impl CollisionGroup {
    /// The schema kind for this class.
    pub const SCHEMA_KIND: SchemaKind = SchemaKind::ConcreteTyped;

    /// The schema type name.
    pub const SCHEMA_TYPE_NAME: &'static str = "PhysicsCollisionGroup";

    // =========================================================================
    // Construction
    // =========================================================================

    /// Construct a CollisionGroup on the given prim.
    pub fn new(prim: Prim) -> Self {
        Self { prim }
    }

    /// Construct a CollisionGroup from another prim.
    pub fn from_prim(prim: &Prim) -> Self {
        Self::new(prim.clone())
    }

    /// Return a CollisionGroup holding the prim at `path` on `stage`.
    pub fn get(stage: &Arc<Stage>, path: &Path) -> Option<Self> {
        let prim = stage.get_prim_at_path(path)?;
        if prim.is_a(&Token::new(Self::SCHEMA_TYPE_NAME)) {
            Some(Self::new(prim))
        } else {
            None
        }
    }

    /// Attempt to ensure a prim adhering to this schema at `path`
    /// is defined on this stage.
    pub fn define(stage: &Arc<Stage>, path: &Path) -> Option<Self> {
        let prim = stage
            .define_prim(path.to_string(), Self::SCHEMA_TYPE_NAME)
            .ok()?;
        Some(Self::new(prim))
    }

    /// Returns the schema kind.
    pub fn get_schema_kind(&self) -> SchemaKind {
        Self::SCHEMA_KIND
    }

    // =========================================================================
    // MergeGroupName Attribute
    // =========================================================================

    /// If non-empty, any collision groups in a stage with a matching
    /// mergeGroup should be considered to refer to the same collection.
    ///
    /// Matching collision groups should behave as if there were a single
    /// group containing referenced colliders and filter groups from both.
    ///
    /// | Property | Value |
    /// |----------|-------|
    /// | Declaration | `string physics:mergeGroup` |
    /// | C++ Type | std::string |
    pub fn get_merge_group_name_attr(&self) -> Option<Attribute> {
        self.prim
            .get_attribute(USD_PHYSICS_TOKENS.physics_merge_group.as_str())
    }

    /// Creates the mergeGroup attribute.
    pub fn create_merge_group_name_attr(&self, default_value: Option<String>) -> Option<Attribute> {
        let attr = self.prim.create_attribute(
            USD_PHYSICS_TOKENS.physics_merge_group.as_str(),
            &usd_sdf::ValueTypeRegistry::instance().find_type_by_token(&Token::new("string")),
            false,
            Some(Variability::Varying),
        )?;

        if let Some(value) = default_value {
            attr.set(Value::from(value), usd_sdf::TimeCode::default());
        }

        Some(attr)
    }

    // =========================================================================
    // InvertFilteredGroups Attribute
    // =========================================================================

    /// Normally, the filter will disable collisions against the selected
    /// filter groups. However, if this option is set, the filter will disable
    /// collisions against all colliders except for those in the selected
    /// filter groups.
    ///
    /// | Property | Value |
    /// |----------|-------|
    /// | Declaration | `bool physics:invertFilteredGroups` |
    /// | C++ Type | bool |
    pub fn get_invert_filtered_groups_attr(&self) -> Option<Attribute> {
        self.prim
            .get_attribute(USD_PHYSICS_TOKENS.physics_invert_filtered_groups.as_str())
    }

    /// Creates the invertFilteredGroups attribute.
    pub fn create_invert_filtered_groups_attr(
        &self,
        default_value: Option<bool>,
    ) -> Option<Attribute> {
        let attr = self.prim.create_attribute(
            USD_PHYSICS_TOKENS.physics_invert_filtered_groups.as_str(),
            &usd_sdf::ValueTypeRegistry::instance().find_type_by_token(&Token::new("bool")),
            false,
            Some(Variability::Varying),
        )?;

        if let Some(value) = default_value {
            attr.set(Value::from(value), usd_sdf::TimeCode::default());
        }

        Some(attr)
    }

    // =========================================================================
    // FilteredGroups Relationship
    // =========================================================================

    /// References a list of PhysicsCollisionGroups with which
    /// collisions should be ignored.
    pub fn get_filtered_groups_rel(&self) -> Option<Relationship> {
        self.prim
            .get_relationship(USD_PHYSICS_TOKENS.physics_filtered_groups.as_str())
    }

    /// Creates the filteredGroups relationship.
    pub fn create_filtered_groups_rel(&self) -> Option<Relationship> {
        self.prim
            .create_relationship(USD_PHYSICS_TOKENS.physics_filtered_groups.as_str(), false)
    }

    // =========================================================================
    // CollectionAPI Access
    // =========================================================================

    /// Return the UsdCollectionAPI interface used for defining
    /// what colliders belong to this CollisionGroup.
    ///
    /// The collection is named "colliders" and maintained on the same prim.
    ///
    /// Matches C++ `UsdPhysicsCollisionGroup::GetCollidersCollectionAPI()`.
    pub fn get_colliders_collection_api(&self) -> Option<CollisionGroupCollectionAPI> {
        if !self.prim.is_valid() {
            return None;
        }
        Some(CollisionGroupCollectionAPI {
            prim: self.prim.clone(),
            collection_name: Token::new("colliders"),
        })
    }

    // =========================================================================
    // Custom Methods
    // =========================================================================

    /// Compute a table encoding all the collision groups filter rules for a stage.
    ///
    /// Handles merge groups (physics:mergeGroup) and inverted filters
    /// (physics:invertFilteredGroups). The returned table is diagonally
    /// symmetric using an upper-triangular layout.
    ///
    /// Matches C++ `UsdPhysicsCollisionGroup::ComputeCollisionGroupTable()`.
    pub fn compute_collision_group_table(stage: &Arc<Stage>) -> CollisionGroupTable {
        // Step 1: Collect all collision groups in stage traversal order.
        // PrimRange implements IntoIterator (not Iterator), so use into_iter().
        let schema_type = Token::new(CollisionGroup::SCHEMA_TYPE_NAME);
        let all_groups: Vec<CollisionGroup> = stage
            .traverse()
            .into_iter()
            .filter(|p| p.get_type_name() == schema_type)
            .map(CollisionGroup::new)
            .collect();

        // Step 2: Assign logical indices, merging groups with the same mergeGroup name.
        // Groups sharing a mergeGroup name get the same logical index.
        let mut prim_path_to_index: HashMap<Path, usize> = HashMap::new();
        let mut merge_group_to_index: HashMap<String, usize> = HashMap::new();
        let mut next_id: usize = 0;

        for cg in &all_groups {
            let path = cg.prim.get_path().clone();
            // C++: if mergeGroupAttr is NOT authored -> unique id.
            // If authored, use mergeGroupName as key (even empty string merges groups).
            let authored_merge_name: Option<String> =
                cg.get_merge_group_name_attr().and_then(|attr| {
                    if attr.has_authored_value_opinion() {
                        attr.get(usd_sdf::TimeCode::default())
                            .and_then(|v| v.downcast_clone::<String>())
                    } else {
                        None
                    }
                });

            if let Some(name) = authored_merge_name {
                // Authored merge group (any string, including empty) -> merge by name
                let id = *merge_group_to_index.entry(name).or_insert_with(|| {
                    let id = next_id;
                    next_id += 1;
                    id
                });
                prim_path_to_index.insert(path, id);
            } else {
                // Not authored -> unique index
                prim_path_to_index.insert(path, next_id);
                next_id += 1;
            }
        }

        // Step 3: Build merged collision table (size = next_id), all enabled.
        let merged_n = next_id;
        let merged_table_size = (merged_n + 1) * merged_n / 2;
        let mut merged: Vec<bool> = vec![true; merged_table_size];

        for cg_a in &all_groups {
            let path_a = cg_a.prim.get_path();
            let idx_a = prim_path_to_index[path_a];

            // Collect filtered group logical indices
            let filtered_indices: Vec<usize> = cg_a
                .get_filtered_groups_rel()
                .map(|rel| {
                    rel.get_targets()
                        .into_iter()
                        .filter_map(|p| prim_path_to_index.get(&p).copied())
                        .collect()
                })
                .unwrap_or_default();

            // Check invertFilteredGroups (only applies when authored)
            let inverted = cg_a
                .get_invert_filtered_groups_attr()
                .and_then(|attr| {
                    if attr.has_authored_value_opinion() {
                        attr.get(usd_sdf::TimeCode::default())
                            .and_then(|v| v.downcast_clone::<bool>())
                    } else {
                        None
                    }
                })
                .unwrap_or(false);

            if !inverted {
                // Normal: disable collision against filteredTargets
                for idx_b in &filtered_indices {
                    let min = idx_a.min(*idx_b);
                    let max = idx_a.max(*idx_b);
                    let ti = CollisionGroupTable::table_index(min, max, merged_n);
                    merged[ti] = false;
                }
            } else {
                // Inverted: disable collision against ALL except filteredTargets
                let allowed: HashSet<usize> = filtered_indices.into_iter().collect();
                for idx_b in 0..merged_n {
                    if !allowed.contains(&idx_b) {
                        let min = idx_a.min(idx_b);
                        let max = idx_a.max(idx_b);
                        let ti = CollisionGroupTable::table_index(min, max, merged_n);
                        merged[ti] = false;
                    }
                }
            }
        }

        // Step 4: Build output table (one entry per actual prim, not logical index).
        let out_n = all_groups.len();
        let out_size = (out_n + 1) * out_n / 2;
        let mut res = CollisionGroupTable {
            groups: all_groups
                .iter()
                .map(|cg| cg.prim.get_path().clone())
                .collect(),
            enabled: vec![true; out_size],
        };

        for i_a in 0..out_n {
            for i_b in i_a..out_n {
                let logical_a = prim_path_to_index[all_groups[i_a].prim.get_path()];
                let logical_b = prim_path_to_index[all_groups[i_b].prim.get_path()];
                let min_l = logical_a.min(logical_b);
                let max_l = logical_a.max(logical_b);
                let merged_ti = CollisionGroupTable::table_index(min_l, max_l, merged_n);
                let out_ti = CollisionGroupTable::table_index(i_a, i_b, out_n);
                res.enabled[out_ti] = merged[merged_ti];
            }
        }

        res
    }

    // =========================================================================
    // Schema attribute names
    // =========================================================================

    /// Returns all pre-declared attributes for this schema class.
    pub fn get_schema_attribute_names(include_inherited: bool) -> Vec<Token> {
        let mut names = if include_inherited {
            Typed::get_schema_attribute_names(true)
        } else {
            Vec::new()
        };

        names.extend([
            USD_PHYSICS_TOKENS.physics_merge_group.clone(),
            USD_PHYSICS_TOKENS.physics_invert_filtered_groups.clone(),
        ]);

        names
    }
}

